// GainNode struct
// js:"GainNode,omit"
type GainNode struct {
}

// Connect fn
// js:"connect"
func (*GainNode) Connect(destination AudioNode, output *uint, input *uint) (a AudioNode) {
	js.Rewrite("$_.connect($1, $2, $3)", destination, output, input)
	return a
}

